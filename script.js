// Obtener el contenedor de las cards
const cardContainer = document.getElementById("cardContainer");

// Recorrer el JSON y generar las cards con logo y botón
for (const aseguradora in jsonData) {
    if (jsonData.hasOwnProperty(aseguradora)) {
        const nombre = aseguradora.toLowerCase(); // Convertir el nombre a minúsculas

        // Crear la tarjeta
        const card = document.createElement("div");
        card.className = "card-container";

        // Crear el contenedor de la imagen
        const imageContainer = document.createElement("div");
        imageContainer.className = "image-container";

        // Crear la imagen (logo) de la aseguradora
        const logoImg = document.createElement("img");
        logoImg.src = `public/${nombre}.png`;
        logoImg.alt = aseguradora;
        logoImg.className = "logo-img"; // Clase personalizada para la imagen
        imageContainer.appendChild(logoImg);

        // Crear el contenedor de los botones
        const buttonContainer = document.createElement("div");
        buttonContainer.className = "button-container";

        // Crear un botón personalizado
        const altaButton = document.createElement("button");
        altaButton.type = "button";
        altaButton.className = "custom-button";
        altaButton.textContent = "Formulario";

        // Agregar un evento al botón para llamar a la función openFileModal
        altaButton.addEventListener("click", () => openFileModal(aseguradora, jsonData[aseguradora]));

        // Agregar la imagen y el botón a sus respectivos contenedores
        imageContainer.appendChild(logoImg);
        buttonContainer.appendChild(altaButton);

        // Agregar los contenedores al cuerpo de la tarjeta
        card.appendChild(imageContainer);
        card.appendChild(buttonContainer);

        // Agregar la tarjeta al contenedor de tarjetas
        cardContainer.appendChild(card);
    }
}

